// Function to get exercises by category for counting
export const getExerciseCountByCategory = (): Record<string, number> => {
  const counts: Record<string, number> = {};
  
  exercises.forEach(exercise => {
    counts[exercise.category] = (counts[exercise.category] || 0) + 1;
  });
  
  return counts;
};

// Update category exercise counts
export const updateCategoryExerciseCounts = (): Omit<ExerciseCategoryInfo, 'createdAt' | 'updatedAt'>[] => {
  const counts = getExerciseCountByCategory();
  
  return exerciseCategories.map(category => ({
    ...category,
    exerciseCount: counts[category.id] || 0,
  }));
};